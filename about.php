and exponential growth of electronic usage leads to major accumulation of e-waste. Our objective is to enhance the ease of individual and organization in efficient e-waste
management in the form of recycle, reuse and refurbish.
The focus being on proper collection of e-waste, recover and recycle material by safe methods, sell and buy electronic
waste, dispose of e-waste by suitable techniques, and raise awareness of the impact of e-waste.</p>
